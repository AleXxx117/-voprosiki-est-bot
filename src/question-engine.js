const codeToKey = new Map(
  Object.entries(categories).map(([key, category]) => [category.code, key])
);

function combinationCount(category) {
  return templates.length
    * category.actions.length
    * category.contexts.length
    * category.lenses.length
    * category.moments.length;
}

function decodeIndex(category, index) {
  const dimensions = [
    category.moments,
    category.lenses,
    category.contexts,
    category.actions,
    templates
  ];

  let rest = index;
  const values = [];
  for (const dimension of dimensions) {
    values.push(dimension[rest % dimension.length]);
    rest = Math.floor(rest / dimension.length);
  }

  return {
    moment: values[0],
    lens: values[1],
    context: values[2],
    action: values[3],
    template: values[4]
  };
}

function render(parts) {
  return parts.template
    .replace("{action}", parts.action)
    .replace("{context}", parts.context)
    .replace("{lens}", parts.lens)
    .replace("{moment}", parts.moment);
}

export function getCategoryKeys() {
  return Object.keys(categories);
}

export function getCategory(key) {
  return categories[key] ?? null;
}

export function getQuestionCount(key) {
  const category = getCategory(key);
  return category ? combinationCount(category) : 0;
}

export function getTotalQuestionCount() {
  return getCategoryKeys().reduce((sum, key) => sum + getQuestionCount(key), 0);
}

export function questionFromIndex(key, index) {
  const category = getCategory(key);
  if (!category) {
    throw new Error(`Неизвестная категория: ${key}`);
  }

  const count = combinationCount(category);
  if (!Number.isInteger(index) || index < 0 || index >= count) {
    throw new RangeError(`Индекс вопроса должен быть от 0 до ${count - 1}`);
  }

  const parts = decodeIndex(category, index);
  return {
    id: `${category.code}_${index.toString(36)}`,
    index,
    categoryKey: key,
    categoryTitle: category.title,
    categoryEmoji: category.emoji,
    text: render(parts)
  };
}

export function questionFromId(id) {
  if (typeof id !== "string" || !/^[a-z]_[0-9a-z]+$/i.test(id)) {
    return null;
  }

  const [code, encodedIndex] = id.toLowerCase().split("_");
  const key = codeToKey.get(code);
  const index = Number.parseInt(encodedIndex, 36);
  if (!key || !Number.isSafeInteger(index)) {
    return null;
  }

  try {
    return questionFromIndex(key, index);
  } catch {
    return null;
  }
}

export function randomQuestion(key = "all", excludedIds = []) {
  const keys = key === "all" ? getCategoryKeys() : [key];
  if (keys.some((candidate) => !getCategory(candidate))) {
    throw new Error(`Неизвестная категория: ${key}`);
  }

  const excluded = new Set(excludedIds);
  for (let attempt = 0; attempt < 40; attempt += 1) {
    const selectedKey = keys[randomInt(keys.length)];
    const question = questionFromIndex(
      selectedKey,
      randomInt(getQuestionCount(selectedKey))
    );
    if (!excluded.has(question.id)) {
      return question;
    }
  }

  const selectedKey = keys[randomInt(keys.length)];
  return questionFromIndex(selectedKey, randomInt(getQuestionCount(selectedKey)));
}

export function formatQuestionCount(value = getTotalQuestionCount()) {
  return new Intl.NumberFormat("ru-RU").format(value);
}
